package j0528;

public class WrapperEx {

	public static void main(String[] args) {
		//Wrapper Class: 자료형을 클래스화 시킨것
		
		byte a1=10;
		short a2=100;
		int a3=200;
		long a4=400;
		float a5=35.123F;
		double a6=12.464;
		boolean a7=true;
		
		//속성, 메소드를 가지는 자료형클래스를 지원 => 다양한 작업이 가능
		Byte b1=10;
		Short b2=100;
		Integer b3=200;
		Long b4=400L;
		Float b5=35.123F;
		Double b6=12.464;
		Boolean b7=true;
		
		Integer imsi=new Integer(100);
	
		

	}

}
